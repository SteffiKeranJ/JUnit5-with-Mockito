package org.example.junitdemo;

public class MathUtils {

    public int add(int a, int b) {
        return a + b;
    }

    public int divide(int a, int b) {
        return a / b;
    }

    public double calculateRadius(int r) {
        return Math.PI * r * r;
    }

    public double multiply(int a, int b) {
        return a * b;
    }
}
